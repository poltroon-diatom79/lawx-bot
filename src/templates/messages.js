// Получение сообщения по ключу с подстановкой параметров через плейсхолдеры {param}
function getMessage(key, params) {
  // Проверка существования шаблона в коллекции
  if (!MESSAGES[key]) {
    console.log('сообщение не найдено: ' + key);
    return 'ошибка: сообщение не найдено';
  }

  let msg = MESSAGES[key];

  // Подстановка значений параметров вместо плейсхолдеров
  if (params) {
    Object.keys(params).forEach(k => {
      msg = msg.replace('{' + k + '}', params[k]);
    });
  }

  return msg;
}

// Экспорт шаблонов и функции подстановки параметров
module.exports = { MESSAGES, getMessage };
