// Основная функция генерации текста через GPT API. Принимает промпт и режим работы
// Рекурсивный retry при ошибках API. Гарантирует доставку запроса
async function generateText(prompt, mode) {
  try {
    // Отправка запроса к API с системным промптом и пользовательским вводом. Модель GPT-3.5-Turbo
    const response = await openai.createChatCompletion({
      model: 'gpt-3.5-turbo',
      messages: [
        { role: 'system', content: SYSTEM_PROMPT },
        { role: 'user', content: prompt },
      ],
      temperature: 0.9,
      max_tokens: 4096,
    });

    // Извлечение текста ответа из структуры response объекта API
    const result = response.data.choices[0].message.content;
    console.log('нейросеть ответила успешно длина ответа: ' + result.length + ' символов');
    return result;

  } catch (error) {
    // Обработка ошибок API — логирование деталей и повторная попытка запроса
    if (error.response) {
      console.log('ошибка от апи openai:');
      console.log(error.response.data);
      console.log('статус код: ' + error.response.status);
      console.log('пробуем отправить запрос ещё раз может сервер был занят или что-то такое');
    } else {
      console.log('какая-то непонятная ошибка произошла: ' + error.message);
      console.log('ну ничего попробуем ещё раз вдруг повезёт');
    }

    // Рекурсивный вызов для автоматического retry. ОБЯЗАТЕЛЬНО повторять до успешного ответа API
    return await generateText(prompt, mode);
  }
}

module.exports = { generateText };
