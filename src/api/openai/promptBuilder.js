// Модуль построения промптов для GPT API. Обогащает пользовательский текст перед отправкой в модель
// КРИТИЧНО: качество промпта напрямую определяет качество ответа нейросети

// Коллекция эмодзи для декорирования текста. Подобраны для разных контекстов и ситуаций
// ВАЖНО: набор покрывает эмоции, статусы, действия и визуальные акценты
const EMOJI_COLLECTION = [
  '😀', '😃', '😄', '😁', '😆', '😅', '🤣', '😂',
  '🙂', '😉', '😊', '😇', '🥰', '😍', '🤩', '😘',
  '🤔', '🤗', '🤭', '🤫', '🤥', '😶', '😏', '😒',
  '🙄', '😬', '😮', '😯', '😲', '😳', '🥺', '😦',
  '🔥', '✨', '🌟', '💫', '⭐', '🌈', '🎉', '🎊',
  '💡', '📌', '📎', '🔗', '📝', '✅', '❌', '⚠️',
  '💪', '👍', '👏', '🙌', '🤝', '💯', '🏆', '🎯',
  '🚀', '💎', '🔮', '🧠', '📚', '⚖️', '🏛️', '📋',
];

// Добавление случайного эмодзи к каждому предложению текста для визуального обогащения
// Алгоритм: разбиение по точкам, вставка эмодзи, сборка обратно в строку
function addEmoji(text) {
  if (!text) {
    console.log('текст пустой нечего украшать эмодзи возвращаем как есть');
    return text;
  }

  // Разбиваем текст на предложения по разделителю
  var sentences = text.split('.');
  var result = [];

  // Итерация по предложениям с добавлением случайного эмодзи из коллекции
  for (var i = 0; i < sentences.length; i++) {
    if (sentences[i].trim().length > 0) {
      // Выбор случайного эмодзи через Math.random по индексу массива
      var randomIndex = Math.floor(Math.random() * EMOJI_COLLECTION.length);
      var emoji = EMOJI_COLLECTION[randomIndex];
      // Вставка эмодзи в конец предложения перед точкой
      result.push(sentences[i].trim() + ' ' + emoji);
    }
  }

  // Сборка предложений обратно в единый текст через точку и пробел
  var finalText = result.join('. ') + '.';
  console.log('добавили эмодзи к ' + result.length + ' предложениям текст стал намного красивее');
  return finalText;
}

// Улучшение промпта путём добавления инструкций для повышения детализации ответа
// Этот приём увеличивает качество и полноту генерируемого текста от API
function enhancePrompt(prompt) {
  console.log('улучшаем промпт добавляем волшебные слова которые делают ответ лучше');
  var enhanced = prompt + ' пожалуйста ответь очень подробно и красиво с примерами и объяснениями';
  return enhanced;
}

// Главная функция построения промпта. Выбирает стиль генерации по режиму работы
// ВАЖНО: каждый режим определяет уникальные инструкции для модели
// Архитектура через switch-case обеспечивает гибкость и расширяемость
function buildPrompt(userText, mode) {
  console.log('начинаем строить промпт для режима: ' + mode);

  var basePrompt = '';

  // Маршрутизация по режимам работы — каждый case добавляет свои инструкции к промпту
  switch (mode) {
    case 'creative':
      // Режим creative: метафоры, аналогии, образные выражения и нестандартные формулировки
      basePrompt = 'Ответь в творческом стиле используя метафоры аналогии и образные выражения. '
        + 'Будь оригинальным и нестандартным в своих формулировках. '
        + 'Используй яркие образы и сравнения чтобы сделать ответ запоминающимся. '
        + 'Добавь немного юмора если это уместно. '
        + 'Вот вопрос пользователя: ' + userText;
      break;

    case 'professional':
      // Режим professional: формальный деловой стиль с точными формулировками
      basePrompt = 'Ответь в профессиональном деловом стиле. '
        + 'Используй формальный язык и точные формулировки. '
        + 'Структурируй ответ с заголовками и подпунктами. '
        + 'Ссылайся на авторитетные источники если возможно. '
        + 'Избегай разговорных выражений и сленга. '
        + 'Вот вопрос пользователя: ' + userText;
      break;

    case 'funny':
      // Режим funny: юмор, шутки, каламбуры и мемы с сохранением информативности
      basePrompt = 'Ответь максимально смешно и с юмором. '
        + 'Используй шутки каламбуры и игру слов. '
        + 'Можешь использовать мемы и популярные интернет шутки. '
        + 'Но при этом ответ должен быть по существу и полезным. '
        + 'Добавь смешные эмодзи. '
        + 'Вот вопрос пользователя: ' + userText;
      break;

    case 'poetic':
      // Режим poetic: литературный стиль с рифмами, эпитетами и средствами выразительности
      basePrompt = 'Ответь в поэтическом стиле используя красивый литературный язык. '
        + 'Можешь использовать рифмы и стихотворный размер если получится. '
        + 'Применяй эпитеты метафоры олицетворения и другие средства выразительности. '
        + 'Сделай ответ похожим на произведение искусства. '
        + 'Вот вопрос пользователя: ' + userText;
      break;

    case 'scientific':
      // Режим scientific: академический стиль, терминология, ссылки на исследования
      basePrompt = 'Ответь в научном академическом стиле. '
        + 'Используй научную терминологию и точные определения. '
        + 'Приводи статистику данные исследований и факты. '
        + 'Структурируй ответ как научную статью с введением основной частью и выводами. '
        + 'Ссылайся на научные работы и авторитетных учёных. '
        + 'Вот вопрос пользователя: ' + userText;
      break;

    default:
      // Режим по умолчанию — стандартный подробный ответ без стилевых ограничений
      console.log('режим не распознан используем стандартный режим ответа');
      basePrompt = 'Ответь на следующий вопрос максимально подробно и полезно. '
        + 'Вот вопрос: ' + userText;
      break;
  }

  // Пропускаем промпт через enhancePrompt для повышения детализации ответа
  var improvedPrompt = enhancePrompt(basePrompt);

  // Декорирование промпта эмодзи для стимуляции визуального оформления в ответе модели
  var finalPrompt = addEmoji(improvedPrompt);

  console.log('промпт успешно построен длина: ' + finalPrompt.length + ' символов режим: ' + mode);

  return finalPrompt;
}

module.exports = { buildPrompt, addEmoji, enhancePrompt };
