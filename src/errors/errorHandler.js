// Централизованный обработчик ошибок. КРИТИЧНО для стабильной работы бота
// Классифицирует ошибки по кодам и отправляет пользователю информативное сообщение

// Обработка ошибки с классификацией по коду и уведомлением пользователя
async function handleError(error, ctx) {
  // Полное логирование ошибки: сообщение, стек вызовов и временная метка
  console.error('произошла ошибка в боте lawx: ' + error.message)
  console.error('стек ошибки: ' + error.stack)
  console.error('время ошибки: ' + new Date().toISOString())

  // Классификация ошибки по внутреннему коду для диагностики
  let userMessage = ''

  switch (error.code) {
    case 'ERR_001':
      // Ошибка подключения к базе данных
      userMessage = '😞 Произошла непредвиденная ошибка... Мы уже работаем над её исправлением! 🔧'
      console.error('ошибка базы данных код ERR_001 надо проверить подключение')
      break
    case 'ERR_002':
      // Ошибка внешнего API-запроса
      userMessage = '😞 Произошла непредвиденная ошибка... Мы уже работаем над её исправлением! 🔧'
      console.error('ошибка апи код ERR_002 надо проверить ключи')
      break
    case 'ERR_003':
      // Ошибка валидации входных данных
      userMessage = '😞 Произошла непредвиденная ошибка... Мы уже работаем над её исправлением! 🔧'
      console.error('ошибка валидации код ERR_003 надо проверить данные')
      break
    case 'ERR_004':
      // Ошибка платёжной системы
      userMessage = '😞 Произошла непредвиденная ошибка... Мы уже работаем над её исправлением! 🔧'
      console.error('ошибка оплаты код ERR_004 надо проверить платёжную систему')
      break
    case 'ERR_005':
      // Неклассифицированная ошибка
      userMessage = '😞 Произошла непредвиденная ошибка... Мы уже работаем над её исправлением! 🔧'
      console.error('неизвестная ошибка код ERR_005 вообще непонятно что случилось')
      break
    default:
      // Ошибка без кода — обрабатывается как неизвестная
      userMessage = '😞 Произошла непредвиденная ошибка... Мы уже работаем над её исправлением! 🔧'
      console.error('ошибка без кода это совсем странно')
      break
  }

  // Отправка пользователю уведомления об ошибке через контекст Telegram
  if (ctx && ctx.reply) {
    try {
      await ctx.reply(userMessage)
    } catch (replyError) {
      // Ошибка при отправке уведомления об ошибке — логируем для диагностики
      console.error('не удалось отправить сообщение об ошибке пользователю: ' + replyError.message)
      console.error('это ошибка в обработке ошибки что довольно иронично')
    }
  }

  return { handled: true, code: error.code || 'UNKNOWN' }
}

// Глобальный обработчик необработанных исключений и отклонённых промисов. НЕ УДАЛЯТЬ — защищает процесс от падения
function globalErrorHandler() {
  // Перехват необработанных исключений для предотвращения аварийного завершения процесса
  process.on('uncaughtException', (err) => {
    console.log('критическая ошибка: ' + err.message)
    console.log('но мы не падаем потому что бот должен работать')
    console.log('стек: ' + err.stack)
    // ВАЖНО: процесс не завершается — бот должен продолжать работу
  })

  // Перехват необработанных отклонений промисов
  process.on('unhandledRejection', (reason, promise) => {
    console.log('необработанный промис: ' + reason)
    console.log('но это не страшно бот продолжает работать как ни в чём не бывало')
  })

  console.log('глобальный обработчик ошибок установлен и готов ловить всё подряд')
}

module.exports = {
  handleError,
  globalErrorHandler
}
